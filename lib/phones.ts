/** Foldable model slugs */
export function isFoldableModel(modelSlug: string): boolean {
  return modelSlug.includes("fold") || modelSlug.includes("flip") || modelSlug.includes("mix-fold");
}

// backward compat shim for old /sell/iphone/* routes
export function calculateBuyBackPrice(
  brand: string, modelSlug: string, storage: string,
  battery: string, issues: string[]
) {
  const answerMap: Record<string, string> = {};
  if (issues.includes("screen_cracked")) answerMap.front_glass = "cracked";
  const grade = classifyCondition(battery, answerMap);
  return getBuyBackPrice(brand, modelSlug, storage, grade);
}

