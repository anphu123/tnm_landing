const GRADE_ORDER: ConditionGrade[] = ["loai-1", "loai-2", "loai-3", "loai-4", "loai-5"];
function worstGrade(a: ConditionGrade, b: ConditionGrade): ConditionGrade {
  return GRADE_ORDER.indexOf(a) >= GRADE_ORDER.indexOf(b) ? a : b;
}

/**
 * Classifies overall device condition based on battery result and questionnaire answers.
 * Final grade = worst (highest Loại) across all answered questions.
 */
export function classifyCondition(
  battery: string,
  answers: Record<string, string>
): ConditionGrade {
  let g: ConditionGrade = "loai-1";

  // Battery: ≥87% → Loại 1, <87% → Loại 4
  if (battery === "below87" || battery === "70-80" || battery === "below70") {
    g = worstGrade(g, "loai-4");
  }

  // Q1 – Tài khoản & Bảo mật
  if (answers.account === "cant_logout")           g = worstGrade(g, "loai-5");

  // Q2 – Khoá mạng SIM
  if (answers.simlock === "locked")                g = worstGrade(g, "loai-5");

  // Q3 – Hiển thị & cảm ứng
  if (answers.screen_display === "broken")         g = worstGrade(g, "loai-5");

  // Q4 – Kính mặt trước
  if      (answers.front_glass === "cracked")      g = worstGrade(g, "loai-5");
  else if (answers.front_glass === "heavy")        g = worstGrade(g, "loai-4");
  else if (answers.front_glass === "many")         g = worstGrade(g, "loai-3");
  else if (answers.front_glass === "few")          g = worstGrade(g, "loai-2");

  // Q5 – Mặt sau
  if      (answers.back === "cracked")             g = worstGrade(g, "loai-5");
  else if (answers.back === "heavy_dent")          g = worstGrade(g, "loai-4");
  else if (answers.back === "many_scratch")        g = worstGrade(g, "loai-3");
  else if (answers.back === "few_scratch")         g = worstGrade(g, "loai-2");

  // Q6 – Khung viền
  if      (answers.frame === "deformed")           g = worstGrade(g, "loai-5");
  else if (answers.frame === "cracked")            g = worstGrade(g, "loai-4");
  else if (answers.frame === "many_scratch")       g = worstGrade(g, "loai-3");
  else if (answers.frame === "few_scratch")        g = worstGrade(g, "loai-2");

  // iPhone-only – Q7: Dynamic Island
  if      (answers.dynamic_island === "jagged")   g = worstGrade(g, "loai-5");
  else if (answers.dynamic_island === "uneven")   g = worstGrade(g, "loai-3");

  // iPhone-only – Q8: Linh kiện
  if      (answers.parts === "unknown_or_lot")    g = worstGrade(g, "loai-5");
  else if (answers.parts === "genuine_repair")    g = worstGrade(g, "loai-2");

  // Foldable-only – Q9: Bản lề
  if      (answers.hinge === "stuck")             g = worstGrade(g, "loai-5");
  else if (answers.hinge === "loose_noisy")       g = worstGrade(g, "loai-4");

  // Foldable-only – Q10: Màn hình gập
  if (answers.fold_screens === "broken")          g = worstGrade(g, "loai-5");

  return g;
}

