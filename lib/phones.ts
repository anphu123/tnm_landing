export function getBrandModels(brandSlug: string) {
  return phoneModels.filter((m) => m.brand === brandSlug);
}

export function getBrand(slug: string) {
  return brands.find((b) => b.slug === slug) ?? null;
}

export function modelToSlug(displayName: string): string {
  // find the slug whose displayName matches
  const entry = Object.entries(MODEL_META).find(([, v]) => v.displayName === displayName);
  if (entry) return entry[0];
  // fallback
  return displayName.toLowerCase().replace(/\s+/g, "-").replace(/[()]/g, "").replace(/\+/g, "plus");
}

export function slugToModelInBrand(brandSlug: string, modelSlug: string) {
  const meta = MODEL_META[modelSlug];
  if (!meta || meta.brand !== brandSlug) return null;
  return phoneModels.find((m) => m.brand === brandSlug && m.name === meta.displayName) ?? null;
}

export function getStorageOptions(brandSlug: string, modelSlug: string): string[] {
  const storages = Object.keys(PRICE_MAP[brandSlug]?.[modelSlug] ?? {});
  const order = ["32gb", "64gb", "128gb", "256gb", "512gb", "1tb", "2tb"];
  return storages.sort((a, b) => order.indexOf(a) - order.indexOf(b));
}

export function storageLabel(slug: string) {
  if (slug === "1tb") return "1 TB";
  if (slug === "2tb") return "2 TB";
  return slug.replace("gb", " GB").toUpperCase();
}

export function getSellPrice(brand: string, modelSlug: string, storage: string): number {
  return PRICE_MAP[brand]?.[modelSlug]?.[storage]?.["loai-1"] ?? 0;
}

